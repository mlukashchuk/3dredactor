/**
 * @brief Модуль пользовательского интерфейса.
 */
class UserInterfaceModule {
public:
    /**
     * @brief Настраивает рабочее пространство.
     * 
     * Этот метод настраивает рабочее пространство, включая расположение и размер окон и панелей.
     * Использует данные конфигурации, сохраненные пользователем.
     */
    void setupWorkspace();

    /**
     * @brief Создает и настраивает панели инструментов и меню.
     * 
     * Метод создает панели инструментов и меню, добавляет на них необходимые элементы управления
     * и настраивает их в соответствии с пользовательскими предпочтениями.
     */
    void setupToolbarsAndMenus();

    /**
     * @brief Предоставляет обучающие материалы и подсказки.
     * 
     * Метод загружает и отображает обучающие материалы, такие как видеоролики или инструкции,
     * а также предоставляет пользователю подсказки и советы по использованию программы.
     */
    void provideLearningMaterialsAndTips();
};